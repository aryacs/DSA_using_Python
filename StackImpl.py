# initial empty stack
stk = []

# append() function to push
# element in the stk
stk.append('x')
stk.append('y')
stk.append('z')

print(stk)

# pop() function to pop
# element from stk in
# LIFO order
print('Elements poped from Stack:')
print(stk.pop())
print(stk.pop())
print(stk.pop())

print('Stack after deletion...')
print(stk)
